#!/usr/bin/env node
/**
 * sync:prettierignore
 *
 * `.harness/manifest.json` 에서 harness-managed 파일 경로를 읽어
 * `.prettierignore` 의 harness 블록을 자동 재생성한다.
 *
 * 배경: harness-setting upstream 의 포맷 컨벤션(double quote 등)과
 * 프로젝트 `.prettierrc.json`(singleQuote: true)이 충돌해
 * lint-staged `prettier --write` 가 upstream 적용을 되돌리는 drift 재발 패턴.
 * ADR: docs/decisions/20260419-prettier-harness-conflict.md
 *
 * 사용:
 *   node scripts/sync-prettierignore.mjs         # 실제 재생성
 *   node scripts/sync-prettierignore.mjs --check # CI 가드용, drift 시 exit 1
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join } from 'node:path';

const ROOT = new URL('..', import.meta.url).pathname;
const MANIFEST = join(ROOT, '.harness/manifest.json');
const IGNORE = join(ROOT, '.prettierignore');

const BEGIN =
  '# --- harness-managed (auto-generated by scripts/sync-prettierignore.mjs, do not edit) ---';
const END = '# --- end harness-managed ---';

// 매니페스트에 포함되어도 prettier 제외 대상에서 예외 (프로젝트 고유 docs)
// ADR §"제외 대상 정책" 참조
const EXCLUDE_PATTERNS = [
  /^docs\/benchmarks\//,
  /^docs\/phases\//,
  /^docs\/reports\//,
  /^docs\/retrospectives\/p[0-9]+[a-z-]*-retrospective\.md$/,
];

// prettier 가 실제로 처리하는 확장자만 제외 목록에 추가 (이미지/바이너리는 제외 불필요)
const PRETTIER_EXTS = /\.(md|json|yaml|yml|ts|tsx|js|jsx|mjs|cjs|css|html)$/;

function loadManifestPaths() {
  if (!existsSync(MANIFEST)) {
    console.error(`[sync-prettierignore] 매니페스트 없음: ${MANIFEST}`);
    process.exit(1);
  }
  const manifest = JSON.parse(readFileSync(MANIFEST, 'utf8'));
  if (!manifest.files || typeof manifest.files !== 'object') {
    console.error('[sync-prettierignore] 매니페스트에 files 필드 없음');
    process.exit(1);
  }
  return Object.keys(manifest.files)
    .filter((p) => !EXCLUDE_PATTERNS.some((re) => re.test(p)))
    .filter((p) => PRETTIER_EXTS.test(p))
    .sort();
}

function buildBlock(paths) {
  const lines = [BEGIN, `# 생성: ${paths.length}개 파일 (매니페스트 기준)`, ...paths, END];
  return lines.join('\n');
}

function replaceBlock(existing, block) {
  const beginIdx = existing.indexOf(BEGIN);
  const endIdx = existing.indexOf(END);

  if (beginIdx === -1 && endIdx === -1) {
    // 블록 없음 — 파일 끝에 추가
    const trimmed = existing.replace(/\n+$/, '');
    return `${trimmed}\n\n${block}\n`;
  }
  if (beginIdx === -1 || endIdx === -1 || endIdx < beginIdx) {
    console.error('[sync-prettierignore] 기존 harness 블록이 손상됨 — 수동 복구 필요');
    process.exit(1);
  }
  const before = existing.slice(0, beginIdx);
  const after = existing.slice(endIdx + END.length);
  const cleanedBefore = before.replace(/\n+$/, '');
  const cleanedAfter = after.replace(/^\n+/, '');
  return `${cleanedBefore}\n\n${block}\n${cleanedAfter ? '\n' + cleanedAfter : ''}`;
}

function main() {
  const checkOnly = process.argv.includes('--check');
  const paths = loadManifestPaths();
  const block = buildBlock(paths);
  const existing = existsSync(IGNORE) ? readFileSync(IGNORE, 'utf8') : '';
  const next = replaceBlock(existing, block);

  if (checkOnly) {
    if (existing !== next) {
      console.error(
        '[sync-prettierignore] drift 감지 — pnpm sync:prettierignore 실행 후 커밋 필요',
      );
      console.error(`  매니페스트 파일 수: ${paths.length}`);
      process.exit(1);
    }
    console.log(`[sync-prettierignore] OK — ${paths.length}개 경로 동기화됨`);
    return;
  }

  if (existing === next) {
    console.log(`[sync-prettierignore] 변경 없음 — ${paths.length}개 경로 유지`);
    return;
  }
  writeFileSync(IGNORE, next);
  console.log(`[sync-prettierignore] 재생성 완료 — ${paths.length}개 경로`);
}

main();
